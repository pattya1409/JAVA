package datatypes;

public class integer {
	public static void main(String[] args) {
		byte a = 20;
		short b = 15000;
		int c = 12345566;
		long d = 1020300330031l;

		System.out.println("byte: " + a);
		System.out.println("short: " + b);
		System.out.println("int: " + c);
		System.out.println("long: " + d);
	}
}

//output
//byte: 20
//short: 15000
//int: 12345566
//long: 1020300330031

