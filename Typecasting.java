package datatypes;

public class Typecasting {
	public static void main(String[] args) {
		int a = 50;
		double b;
		b = a;
		System.out.println(a);
		System.out.println(b);
		
		double c = 123.456;
		int d;
		d = (int)c;
		System.out.println(c);
		System.out.println(d);
	}
}

//output
//50
//50.0

//123.456
//123

